a="Hello world"
print(a.capitalize()) #capital  the word
print(a.lower()) #lower case the word
print(a.upper()) #upper case the word

b="     hello world      "
print(b.strip()) #to remove the space from the first and last of the words
print(b.lstrip()) #to remove the space from the left side of the word
print(b.rstrip()) #to remove space from the right side of the word

c="Hello world"
print(c.replace("Hello","hai")) #to replace first word by second word
